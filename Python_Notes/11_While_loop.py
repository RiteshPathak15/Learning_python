#The Python while loop iteration of a code block is executed as long as the given Condition, i.e., conditional_expression, is true.
#python does not support Do-while loop
#In python you can use While loop with else block
#Syntax:
# while Condition:  
#         Statement  
# Printing 1 to 10
i=0
while(i<=10):
    print(i)
    i=i+1
print("End")

